      fetchUserFromServer(userData.id);
    } catch (error) {
      console.error("–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–∞—Ä—Å–∏–Ω–≥–µ tgWebAppData:", error);
      userNotFound.value = true;
    }
  } else {
    console.warn("tgWebAppData –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ URL.");
    userNotFound.value = true;
  }
});
</script>

